#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	numbers[5];

	i = 0;
	while (i < 99)
	{
		j = i + 1;
		while (j < 100)
		{
			numbers[0] = (i / 10) + '0';
			numbers[1] = (i % 10) + '0';
			numbers[2] = ' ';
			numbers[3] = (j / 10) + '0';
			numbers[4] = (j % 10) + '0';
			write(1, numbers, 5);
			if (i != 98)
			{
				write(1, ", ", 2);
			}
			j++;
		}
		i++;
	}
}
