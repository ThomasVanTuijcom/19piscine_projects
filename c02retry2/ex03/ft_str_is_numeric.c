int	ft_str_is_numeric(char *str)
{
	int	value;
	int	i;

	value = 1;
	i = 0;
	while (value == 1 && str[i] != '\0')
	{
		if (str[i] < 48 || str[i] > 57)
			value = 0;
		i++;
	}
	if (i == 0)
		return (1);
	else
		return (value);
}
